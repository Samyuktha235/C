#include <stdio.h>
int main()
{
    int int_a=2,int_b=5;
    float float_a=2.3,float_b=3.2;
    char char_a='a',char_b='b';
    printf("Addition:\n");
    printf("int_a+int_b=%d\n",int_a+int_b);
    printf("float_a+float_b=%f\n",float_a+float_b);
    printf("char_a+char_b=%d\n\n",char_a+char_b);
    printf("Subtraction:\n");
    printf("int_a-int_b=%d\n",int_a-int_b);
    printf("float_a-float_b=%f\n",float_a-float_b);
    printf("char_a-char_b=%d\n\n",char_a-char_b);
    printf("Multiplication:\n");
    printf("int_a*int_b=%d\n",int_a*int_b);
    printf("float_a*float_b=%f\n",float_a*float_b);
    printf("char_a*char_b=%d\n\n",char_a*char_b);
    printf("Division:\n");
    printf("int_a/int_b=%d\n",int_a/int_b);
    printf("float_a/float_b=%f\n",float_a/float_b);
    printf("char_a/char_b=%d\n\n",char_a/char_b);


    return 0;
}

